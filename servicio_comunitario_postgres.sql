CREATE TYPE calle_enum       AS ENUM ('Calle 1', 'Calle 2', 'Calle 3', 'Calle 4', 'Calle 5', 'Calle 6', 'Calle 7', 'Callejón');

-- --- Tablas ---
CREATE TABLE estados_civiles (
    id_estado_civil integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    estado_civil    varchar(20) NOT NULL
);

CREATE TABLE roles (
    id_rol     integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre_rol varchar(50) NOT NULL
);

CREATE TABLE personas (
    id_persona      integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    cedula          varchar(15) NOT NULL UNIQUE,
    nombre          varchar(100) NOT NULL,
    apellido        varchar(100) NOT NULL,
    sexo            sexo_enum NOT NULL,
    edad            integer,
    id_estado_civil integer,
    celular         varchar(20),
    carga_familiar  integer DEFAULT 0,
    fecha_registro  timestamp DEFAULT CURRENT_TIMESTAMP,
    calle           calle_enum,
    estatus         varchar(20) DEFAULT 'Activo'
);

CREATE TABLE usuarios (
    id_usuario    integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_persona    integer NOT NULL,
    id_rol        integer NOT NULL,
    usuario_login varchar(50) NOT NULL UNIQUE,
    password_hash varchar(255) NOT NULL
);

CREATE TABLE registro_bombonas (
    id_registro         integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_persona          integer NOT NULL,
    bombonas_10kg       integer DEFAULT 0,
    bombonas_18kg       integer DEFAULT 0,
    bombonas_27kg       integer DEFAULT 0,
    fecha_actualizacion timestamp DEFAULT CURRENT_TIMESTAMP,
    bombonas_43kg       integer DEFAULT 0
);

CREATE TABLE pagos_bombonas (
    id_pago          integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_registro      integer,
    monto_pagado     numeric(10,2) NOT NULL,
    metodo_pago      metodo_pago_enum NOT NULL,
    fecha_pago       timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
    cant_10kg        integer DEFAULT 0,
    cant_18kg        integer DEFAULT 0,
    cant_27kg        integer DEFAULT 0,
    referencia_texto varchar(100),
    referencia_foto  varchar(255),
    cant_43kg        integer DEFAULT 0
);

