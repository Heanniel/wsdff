-- --- Trigger que replica ON UPDATE current_timestamp() de MySQL ---
CREATE OR REPLACE FUNCTION set_fecha_actualizacion()
RETURNS trigger AS $$
BEGIN
    NEW.fecha_actualizacion = CURRENT_TIMESTAMP;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trg_registro_bombonas_actualizacion
    BEFORE UPDATE ON registro_bombonas
    FOR EACH ROW
    EXECUTE FUNCTION set_fecha_actualizacion();

